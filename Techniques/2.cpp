#include <bits/stdc++.h>
using namespace std;
#define PI 3.14
#define RAD 5.0
#define HEIGHT 10.0

int main()
{
    cout << 2.0 * RAD * PI << endl;
    cout << RAD * RAD * PI << endl;
    cout << RAD * RAD * PI * HEIGHT << endl;
    cout << 4.0 * RAD * RAD * PI << endl;
    cout << 4.0 / 3.0 * RAD * RAD * RAD * PI << endl;

    return 0;
}